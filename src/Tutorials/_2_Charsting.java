
package Tutorials;

public class _2_Charsting {

	public static void main(String[] args) {
		System.out.println('코'); /* 문자는 작은 따옴표로*/
		System.out.println("코딩"); /* 문자열은 큰따옴표로*/
		System.out.println("코딩" + "합시다"); /*문자열 더하기 가능 */
		System.out.println("1"+"1"); /* 11이 출력됨 */
		System.out.println("\"코딩\"합시다"); /* 문자열안에 큰 따옴표를 넣고 싶으면 내부에 \" \" 사용 */
		System.out.println("코딩 \n합시다"); /* \n : 줄바꿈 */

	}

}
